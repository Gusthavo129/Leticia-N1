public class ExemploPratico {
    public static void main(String[] args){
        final float pi = 314156f;
        final double e;

        e = 1.234e2;

        String nome = "1234";
        String dataAniversario = "20032005";
        int lote = 3545;
        boolean aprovado;

        aprovado = false;

        System.out.println(pi); //vai imprimir "3.146f"
        System.out.println(e); //vai imprimir "1.234e2"
        System.out.println(nome);  //vai imprimir "Gusthavo Reberte"
        System.out.println(dataAniversario); //vai imprimir "20/03/2005"
        System.out.println(lote); //vai imprimir "35456"
        System.out.println(aprovado); //vai imprimir "true"
    }
}